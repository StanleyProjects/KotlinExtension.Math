package sp.kx.math

/**
 * Stores two values. Can be used to store the coordinates of a point in 2D space.
 * @property x The coordinate of the x-axis.
 * @property y The coordinate of the y-axis.
 * @author [Stanley Wintergreen](https://github.com/kepocnhh)
 * @since 0.2.0
 */
interface Point {
    val x: Double
    val y: Double
}
